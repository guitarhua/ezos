// GUI X Time
int GUI_X_GetTime(void)
{
    return delay_tick_get();
}

void GUI_X_Delay(int ms)
{
    // GUIDEMO 会在中断中使用延时函数, 不能使用delay_ms
    // delay_ms(ms);

    int i;
    for (; ms>0; ms--)
        for (i=100; i>0; i--) __NOP();
}

